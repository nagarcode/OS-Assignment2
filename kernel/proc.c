int sigaction(int signum, const struct sigaction *act, struct sigaction *oldact)
{
  int sanity;
  struct proc *p = myproc();
  struct sigaction kernelAct;
  if (signum < 0 || signum >= 32 || act == 0)
    return -1;
  if (signum == SIGKILL || signum == SIGSTOP)
    return -1;
  if (oldact)
  {
    void *handler = (void *)p->signal_handlers[signum];
    printf("getting old handler and mask values from kernel to user\n");
    sanity = either_copyout(1, (uint64)oldact->sa_handler, handler, sizeof(sighandler_type)); //TODO this does not work
    if (sanity < 0)
      panic("Copyout failed in sigaction\n");
    sanity = either_copyout(1, oldact->sigmask, &p->handler_masks[signum], sizeof(uint32));
    if (sanity < 0)
      panic("Copyout failed in sigaction\n");
    oldact->sa_handler = p->signal_handlers[signum];
  }
  if (act)
  {
    if ((kernelAct.sigmask & (1 << SIGKILL)) || (kernelAct.sigmask & (1 << SIGSTOP)))
      return -1;
    sanity = either_copyin(&kernelAct, 1, (uint64)act, sizeof(struct sigaction));
    if (sanity < 0)
      panic("Copyin failed in sigaction\n");
    p->signal_handlers[signum] = kernelAct.sa_handler;
    p->handler_masks[signum] = kernelAct.sigmask;
    // printf("Received mask: %d\n", kernelAct.sigmask);
    // printf("Received handler address: %d\n", kernelAct.sa_handler);
  }

  return 0;
}

uint sigprocmask(uint sigmask)
{
  struct proc *p = myproc();
  if (p == 0 || sigmask < 0)
    panic("SIGPROCMASK ERROR");
  if ((sigmask & (1 << SIGKILL)) || (sigmask & (1 << SIGSTOP)))
    return -1;
  uint prevMask = p->signal_mask;
  p->signal_mask = sigmask;
  return prevMask;
}

// Allocate a page for each process's kernel stack.
// Map it high in memory, followed by an invalid
// guard page.
void proc_mapstacks(pagetable_t kpgtbl)
{
  struct proc *p;

  for (p = proc; p < &proc[NPROC]; p++)
  {
    char *pa = kalloc();
    if (pa == 0)
      panic("kalloc");
    uint64 va = KSTACK((int)(p - proc));
    kvmmap(kpgtbl, va, (uint64)pa, PGSIZE, PTE_R | PTE_W);
  }
}

// initialize the proc table at boot time.
void procinit(void)
{
  struct proc *p;

  initlock(&pid_lock, "nextpid");
  initlock(&wait_lock, "wait_lock");
  for (p = proc; p < &proc[NPROC]; p++)
  {
    initlock(&p->lock, "proc");
    p->kstack = KSTACK((int)(p - proc));
  }
}

// Must be called with interrupts disabled,
// to prevent race with process being moved
// to a different CPU.
int cpuid()
{
  int id = r_tp();
  return id;
}

// Return this CPU's cpu struct.
// Interrupts must be disabled.
struct cpu *
mycpu(void)
{
  int id = cpuid();
  struct cpu *c = &cpus[id];
  return c;
}

// Return the current struct proc *, or zero if none.
struct proc *
myproc(void)
{
  push_off();
  struct cpu *c = mycpu();
  struct proc *p = c->proc;
  pop_off();
  return p;
}

int allocpid()
{
  int pid;

  acquire(&pid_lock);
  pid = nextpid;
  nextpid = nextpid + 1;
  release(&pid_lock);

  return pid;
}
void init_handlers(struct proc *p)
{
  for (int i = 0; i < 32; i++) //initial handler is SIG_DFL
  {
    p->signal_handlers[i] = SIG_DFL;
  }
}
void init_handler_masks(struct proc *p)
{
  for (int i = 0; i < 32; i++)
  {
    p->handler_masks[i] = 0;
  }
}

// Look in the process table for an UNUSED proc.
// If found, initialize state required to run in the kernel,
// and return with p->lock held.
// If there are no free procs, or a memory allocation fails, return 0.
static struct proc *
allocproc(void)
{
  struct proc *p;

  for (p = proc; p < &proc[NPROC]; p++)
  {
    acquire(&p->lock);
    if (p->state == UNUSED)
    {
      goto found;
    }
    else
    {
      release(&p->lock);
    }
  }
  return 0;

found:
  p->pid = allocpid();
  p->state = USED;

  // Allocate a trapframe page.
  if ((p->trapframe = (struct trapframe *)kalloc()) == 0)
  {
    freeproc(p);
    release(&p->lock);
    return 0;
  }

  // An empty user page table.
  p->pagetable = proc_pagetable(p);
  if (p->pagetable == 0)
  {
    freeproc(p);
    release(&p->lock);
    return 0;
  }

  // Set up new context to start executing at forkret,
  // which returns to user space.
  memset(&p->context, 0, sizeof(p->context));
  p->context.ra = (uint64)forkret;
  p->context.sp = p->kstack + PGSIZE;
  //*** Added Code - SIGS ***
  init_handlers(p);
  init_handler_masks(p);
  p->frozen = 0;
  //*** /Added Code - SIGS ***

  return p;
}

// free a proc structure and the data hanging from it,
// including user pages.
// p->lock must be held.
static void
freeproc(struct proc *p)
{
  if (p->trapframe)
    kfree((void *)p->trapframe);
  p->trapframe = 0;
  if (p->pagetable)
    proc_freepagetable(p->pagetable, p->sz);
  p->pagetable = 0;
  p->sz = 0;
  p->pid = 0;
  p->parent = 0;
  p->name[0] = 0;
  p->chan = 0;
  p->killed = 0;
  p->xstate = 0;
  p->state = UNUSED;
}

// Create a user page table for a given process,
// with no user memory, but with trampoline pages.
pagetable_t
proc_pagetable(struct proc *p)
{
  pagetable_t pagetable;

  // An empty page table.
  pagetable = uvmcreate();
  if (pagetable == 0)
    return 0;

  // map the trampoline code (for system call return)
  // at the highest user virtual address.
  // only the supervisor uses it, on the way
  // to/from user space, so not PTE_U.
  if (mappages(pagetable, TRAMPOLINE, PGSIZE,
               (uint64)trampoline, PTE_R | PTE_X) < 0)
  {
    uvmfree(pagetable, 0);
    return 0;
  }

  // map the trapframe just below TRAMPOLINE, for trampoline.S.
  if (mappages(pagetable, TRAPFRAME, PGSIZE,
               (uint64)(p->trapframe), PTE_R | PTE_W) < 0)
  {
    uvmunmap(pagetable, TRAMPOLINE, 1, 0);
    uvmfree(pagetable, 0);
    return 0;
  }

  return pagetable;
}

// Free a process's page table, and free the
// physical memory it refers to.
void proc_freepagetable(pagetable_t pagetable, uint64 sz)
{
  uvmunmap(pagetable, TRAMPOLINE, 1, 0);
  uvmunmap(pagetable, TRAPFRAME, 1, 0);
  uvmfree(pagetable, sz);
}

